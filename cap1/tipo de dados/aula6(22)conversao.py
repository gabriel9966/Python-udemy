# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
# str, int, float, bool


print('1', type('1'))
print(type(int('1')))
print(int('1') + 1)
print(bool(''))
print(1 + 1)
print(str(1))
print(1 + 1.0)
print(str(1),str(),bool(1),bool())
print(str(True),str(False))